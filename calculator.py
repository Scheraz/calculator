def add(n1, n2):
    return n1 + n2
def subtract(n1,n2):
    return n1 - n2
def multiply(n1,n2):
    return n1 * n2
def divide(n1,n2):
    return n1 / n2

operation = {
    "+" : add,
    "-" : subtract,
    "*" : multiply,
    "/" : divide}

def calculator():
    should_accumulate = True
    num1 = float(input("Enter First Number: "))
    for symbol in operation:
        print(symbol)
    while should_accumulate:
        operation_symbol = input("Pick any operation: ")
        num2 = float(input("What's the second number?: "))
        answer = operation[operation_symbol](num1, num2)
        print(f"{num1} {operation_symbol} {num2} = {answer}")
        choice = input("Type 'y' to continue calculating with 8.0, or type 'n' to start a new calculation:")

        if choice == "y":
            num1 = answer
        elif choice == "n":
            should_accumulate = False
            calculator()
calculator()





